import turtle

turtle.bgcolor("black")
turtle.color("white")

turtle.penup()
turtle.goto(160, -180 )
turtle.pendown()

turtle.begin_fill()
turtle.forward(100)
turtle.right(90)
turtle.forward(100)
turtle.right(90)
turtle.forward(100)
turtle.right(90)
turtle.forward(100)
turtle.right(90)
turtle.forward(50)
turtle.end_fill()

turtle.left(90)
turtle.forward(300)

turtle.done()